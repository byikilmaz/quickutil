export const useTranslations = (namespace: string, locale: string = 'tr') => {
  return (key: string) => {
    const fullKey = namespace ? `${namespace}.${key}` : key;
    return (translations as any)[locale]?.[fullKey] || (translations as any)['tr']?.[fullKey] || key;
  };
};

export const getTranslations = (locale: string) => {
  // DEBUG - Always log for translation debugging
  console.log('🔍 getTranslations - Input locale:', locale);
  console.log('🔍 getTranslations - Available locales:', Object.keys(translations));
  
  const requestedTranslations = translations[locale as keyof typeof translations];
  const fallbackTranslations = translations.tr;
  const result = requestedTranslations || fallbackTranslations;
  
  console.log('🔍 getTranslations - Results:', {
    locale,
    hasRequestedTranslations: !!requestedTranslations,
    hasFallbackTranslations: !!fallbackTranslations,
    usingFallback: !requestedTranslations,
    resultType: typeof result,
    resultHasAuthWelcomeTitle: !!(result as any)?.['auth.welcomeTitle'],
    authWelcomeTitleValue: (result as any)?.['auth.welcomeTitle']
  });
  
  return result;
}; 